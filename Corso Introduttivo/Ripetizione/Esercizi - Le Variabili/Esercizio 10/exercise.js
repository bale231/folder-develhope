const firstYearCompleted = true;
const yearsCompleted = 1;

console.log(yearsCompleted == firstYearCompleted); //Qui il risultato è "true" perchè viene utilizzato un operatore che stabilisce se le due variabili siano semplicemente simili;
console.log(yearsCompleted === firstYearCompleted) //Qui, invece, viene utilizzato un operatore che stabilisce se le due variabili siano esattamente identiche o meno, quindi di conseguenza il risultato sarà "false".